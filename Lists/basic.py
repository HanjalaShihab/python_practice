#List is a collection which is ordered and changeable. Allows duplicate members.

#Creating a list:
myList = ["apple", "banana", "cherry"]
print(myList)



#list allows duplicate values:
newList = ['shihab', 'hanjala', 'shihab', 'muhammad']
print(newList)



#len of a list:
myList = [1, 2, 3, 4, 5]
print(len(myList))
myList2 = ['shihab', 'hanjala', 'muhammad']
print(len(myList2))



#list allows different data types:
items = ["apple", 1,2, True, 3.1416]
print(items)



#type of a list:
print(type(items))
